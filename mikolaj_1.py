# --- FUNKCJA RYSUJCA ---
def draw_santa():
    # Konfiguracja wykresu (tworzymy figur i osie)
    fig, ax = plt.subplots(figsize=(8, 10))

    # Ustawienia osi
    ax.set_aspect('equal')
    ax.set_xlim(-15, 15)
    ax.set_ylim(-25, 12)
    ax.axis('off') # Ukrywamy osie

    # --- MIKOAJ ---

    # --- CZAPKA ---
    # G贸wna cz czapki (tr贸jkt)
